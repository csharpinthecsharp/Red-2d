int format_check(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[i] == '.') && 
			(str[i + 1] == 'b') &&
			(str[i + 2] == 'e') &&
			str[i + 3] == 'r')
			return (1);
		i++;
	}
	return (0);
}